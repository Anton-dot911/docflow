def is_demo_document_id(document_id: UUID) -> bool:
    """True if `document_id` is one of the 5 fixed demo documents."""
    return document_id in DEMO_DOCUMENT_IDS


def is_demo_user(user_id: UUID) -> bool:
    """True if `user_id` is the fixed demo user (see `app.config.DEMO_USER_ID`)."""
    return user_id == DEMO_USER_ID
