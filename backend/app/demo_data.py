"""Static metadata for the 5 curated demo documents (T9).

Fixed `document_id`s so seeding is idempotent (re-running the seed script
recognizes existing rows by id instead of creating duplicates) and so both
`scripts/seed_demo.py` and `GET /api/demo/samples` (`app/routes/demo.py`) share
one source of truth for filenames/difficulty/description. Fixture *bytes* are
generated by the seed script itself (see docs/decisions.md); this module only
carries the metadata needed to seed and to render the /demo entry page cards.
"""

from __future__ import annotations

from dataclasses import dataclass
from uuid import UUID

from app.config import DEMO_USER_ID

__all__ = ["DEMO_DOCUMENTS", "DEMO_DOCUMENT_IDS", "DemoDocSpec"]


@dataclass(frozen=True)
class DemoDocSpec:
    """One curated demo document: fixed id + display metadata for the card."""

    key: str
    document_id: UUID
    filename: str
    difficulty: str
    title: str
    description: str


